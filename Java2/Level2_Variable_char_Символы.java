package Java2;

public class Level2_Variable_char_Символы {
    public static void main(String[] args) { 
        System.out.println("Переменные и типы данных!"); 
        char character = '&'; /** Ввели тип данных char - символы, и назвали переменную "character" присвоили значение = '&', может хранить тип данных - только один символ */
        System.out.println("Символ: " + character + "."); /** Вывели значение первое присвоенное, и вставили текст для лучшего восприятия */
    }
}

package Java2;

public class Level2_Variable_int_Целые_числа {
    public static void main(String[] args) {  
        System.out.println("Переменные и типы данных!");
        int age = 56; /** Ввели тип данных int - целое число, и назвали переменную "age" присвоили значение = 56 */
        System.out.println("Возраст: " + age + " лет."); /** Вывели значение первое присвоенное, и вставили текст для лучшего восприятия */
        age = 45;
        System.out.println(age); /** Вывели значение второе присвоенное */
    }
}

        System.out.println(symbols[0][0]);
    
        byte[][] numbers = new byte[][] {
            {8, 7}, /** указываем фигурные скобки чтобы создать ещё один массив и указываем значения, которые должны совпадать между собой по количеству */
            {6, 1}, /** {8, 7} - это 0 индекс - первый элемент */
            {4, 3}  /** {6, 1} - это 1 индекс - второй элемент */
        };          /** {4, 3} - это 2 индекс - третий элемент */

       // numbers[1][1] = 67; /** обращаемся ко второму элементу, и можно установить новое значение, либо вывести элемент нужный */

                                           /** первая [2]-обозначает: {4, 3} - элемент; вторая скобка [0]-обозначает в элементе {4, 3}- цифру "4" */
           // for()  /** чтобы перебрать двумерный массив нужно будет создать два цикла "for", так как один цикл будет перебирать один массив, вложенный внутри другого */
              //  for() /** чтобы перебрать трёхмерный массив нужно будет создать уже три цикла "for" */
